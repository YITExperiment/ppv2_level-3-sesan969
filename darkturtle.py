import turtle


from itertools import cycle
colors =cycle(['red','orange','yellow','green','blue','purple'])

def draw_circle(size,angle,shift):size,angle,shift
turtle.bgcolor(next(colors))
turtle.pencolor(next(colors))
turtle.circle(30)
turtle.right(35)
turtle.forward(40)
draw_circle(45,50,55)

turtle.bgcolor('black')
turtle.speed('fast')
turtle.pensize(40)
draw_circle(30,0,1)



